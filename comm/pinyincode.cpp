bool isTextUnicode(const QString &file)
{
    QFile f(file);
    if (f.open(QIODevice::ReadOnly | QIODevice::Text)) {

        QByteArray ba = f.readAll();
        f.close();

        QTextCodec::ConverterState state;
        QTextCodec *codec = QTextCodec::codecForName("UTF-8");
        codec->toUnicode( ba.constData(), ba.size(), &state);
        return (state.invalidChars == 0);
    }
    return false;
}

QString ChineseConvertor::GetFirstLetter(const QString &src)
{
    QString letters;
    for ( int i = 0, iLen = src.length(); i < iLen; ++i ) {

        ushort unicode = src.at(i).unicode();

        if ( unicode > 32 && unicode < 127 ) {
            letters += QChar(unicode);
        }
        else {
            int idx = unicode - HANZI_START;
            if ( idx >= 0 && idx < HANZI_COUNT ) {
                letters += QChar(kanjiFirstLetters[idx]);
            }
        }
    }
    return letters.toUpper();
}

